vim.g.mapleader = " "
vim.keymap.set("n", "<leader>pv", vim.cmd.Ex)

-- Just like ALT-ARROW in vs code
vim.keymap.set("v", "J", ":m '>+1<CR>gv=gv")
vim.keymap.set("v", "K", ":m '<-2<CR>gv=gv")

vim.keymap.set("n", "J", "mzJ`z") -- Place line below on same line adding space
vim.keymap.set("n", "<C-d>", "<C-d>zz") -- Movement page down while center cursor
vim.keymap.set("n", "<C-u>", "<C-u>zz") -- Movement page up while center cursor
vim.keymap.set("n", "n", "nzzzv")
vim.keymap.set("n", "N", "Nzzzv")

-- greatest remap ever
vim.keymap.set("x", "<leader>p", [["_dP]])

